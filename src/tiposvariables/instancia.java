public class instancia {

        
        private String nombre;
        private String trabajo;
        
        //Constructor
       public instancia (String nombre, String trabajo){
           this.nombre = nombre;
           this.trabajo = trabajo;
           
       }
       public String getNombre(){
           return nombre;
           
      
           
       }

    public String getTrabajo() {
        return trabajo;
    }
    }


    
    
    
